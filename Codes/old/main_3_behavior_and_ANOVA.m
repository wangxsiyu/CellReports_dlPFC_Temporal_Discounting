%% shuffled
% jobs.power_on;
infiles = {'EPOCH_sigcells', 'EPOCH_nonsigcells', 'EPOCH_sigcells_shuffled', ...
    'EPOCH_sigcells_shuffled_rate', 'EPOCH_sigcells_shuffled_trial', ...
    'EPOCH_sigcells_final_non0'};
