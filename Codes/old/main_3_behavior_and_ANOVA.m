    {'anova_go'}, {}, 'anova_go_allcells');
% jobs.power_off;
%% behavioral modeling of value
modelnames = ["base", "YP"];
for i = 1:1 % 2
    jobs.add_jobs_with_looper_folder([], 'function_model', ...
        {'GAMES_final'}, {i}, {sprintf('result_RL_%s', modelnames(i))});
