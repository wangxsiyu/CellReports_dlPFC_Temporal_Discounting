looper = S_looper_folder('../../Data/');
jobs = S_jobs();
jobs.set_loopers({'folder'}, {looper});
jobs.power_off;
jobs.add_jobs_with_looper_folder([], 'event2trials', ...
    {'events'}, {'inputname1'}, 'trials');
jobs.add_jobs_with_looper_folder([], 'preprocess_trials', ...
    {'trials'}, {}, 'games');
%% cue period
% epoch cue -600 to 1100
jobs.add_jobs_with_looper_folder([], 'W.epoch_spikebinning_by_marker', ...
    {'spikes', 'trials'}, {1199, 1, -600:10:1100, 100}, 'epoch_cueon_600_1000');
jobs.power_on;
jobs.add_jobs_with_looper_folder([], 'W.select_trials_cellgame', ...
    {'epoch_cueon_600_1000','games'},  {}, ...
    {'EPOCH_cue','games_cue'}, 'inputfunc', @(ev, g){ev, g, g.is_complete & ~g.is_post_error});
jobs.add_jobs_with_looper_folder({'all', 'all'}, 'W.format_combinecells_and_games', ...
    {'EPOCH_cue', 'games_cue'}, {}, 'cue_all');
jobs.power_off;
% epoch cue spike trains
jobs.add_jobs_with_looper_folder([], 'W.epoch_spiketrains_by_marker', ...
    {'spikes', 'trials'}, {1199, 1, -600, 1100, 1}, 'epoch_st_cueon_600_1000');
jobs.power_on;
jobs.add_jobs_with_looper_folder([], 'W.select_trials_cellgame', ...
    {'epoch_st_cueon_600_1000','games'},  {}, ...
    {'ST_cue'}, 'inputfunc', @(ev, g){ev, g, g.is_complete & ~g.is_post_error});
jobs.add_jobs_with_looper_folder({'all', 'all'}, 'W.format_combinecells_and_games', ...
    {'ST_cue', 'games_cue'}, {}, 'ST_cue_all');
jobs.power_off;
%% go period
jobs.add_jobs_with_looper_folder([], 'W.epoch_spikebinning_by_marker', ...
    {'spikes', 'trials'}, {[1038 1121], 1, -600:10:1100, 100}, 'epoch_go_600_1100');
jobs.power_on;
jobs.add_jobs_with_looper_folder([], 'W.select_trials_cellgame', ...
    {'epoch_go_600_1100','games'},  {}, ...
    {'EPOCH_go','games_go'}, 'inputfunc', @(ev, g){ev, g, g.is_complete & ~g.is_post_error});
jobs.add_jobs_with_looper_folder({'all', 'all'}, 'W.format_combinecells_and_games', ...
    {'EPOCH_go', 'games_go'}, {}, 'go_all');
jobs.power_off;
jobs.add_jobs_with_looper_folder([], 'W.epoch_spiketrains_by_marker', ...
    {'spikes', 'trials'}, {[1038 1121], 1, -600, 1100, 1}, 'epoch_st_go_600_1000');
jobs.power_on;
jobs.add_jobs_with_looper_folder([], 'W.select_trials_cellgame', ...
    {'epoch_st_go_600_1000','games'},  {}, ...
    {'ST_go'}, 'inputfunc', @(ev, g){ev, g, g.is_complete & ~g.is_post_error});
jobs.add_jobs_with_looper_folder({'all', 'all'}, 'W.format_combinecells_and_games', ...
    {'ST_go', 'games_go'}, {}, 'ST_go_all');
jobs.power_off;
%%
jobs.parfor_on;
jobs.overwrite_on;
jobs.run()
%% merge ST and EPOCH
data = looper.looper_loadmix({'ST_cue_all','ST_go_all','cue_all', 'go_all'}, 'struct');
assert(isequal(data.cue_all.info_cells, data.ST_cue_all.info_cells));
assert(isequal(data.go_all.info_cells, data.ST_go_all.info_cells));
cue = data.ST_cue_all;
cue.ST = cue.cells;
cue.cells = data.cue_all.cells;
cue = W.struct_merge(cue, data.cue_all, 'ignore');
looper.looper_save_master(cue, 'cue');
go = data.ST_go_all;
go.ST = go.cells;
go.cells = data.go_all.cells;
go = W.struct_merge(go, data.go_all, 'ignore');
looper.looper_save_master(go, 'go');

















